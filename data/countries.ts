/** ISO 3166-1 alpha-2 country reference data (codes/names/flags only — NOT content). */
export interface Country {
  code: string; // ISO 3166-1 alpha-2, uppercase
  name: string;
  flag: string; // emoji
}

/** The flag emoji for an uppercase 2-letter ISO code (two Regional Indicator symbols). */
function flagOf(code: string): string {
  if (!/^[A-Z]{2}$/.test(code)) throw new Error("flagOf: expected an uppercase 2-letter code, got " + code);
  return String.fromCodePoint(...[...code].map((ch) => 0x1f1e6 + ch.charCodeAt(0) - 65));
}

